package org.newstextanalyzer;

public class WordsPair {
  private String word1;
  private String word2;

  public WordsPair(String word1, String word2) {
    this.word1 = word1;
    this.word2 = word2;
  }

  public String getWord1() {
    return this.word1;
  }

  public String getWord2() {
    return this.word2;
  }
}
